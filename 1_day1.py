'''
Basic Built-in Collections
Lists - Ordered, mutable sequences that can hold mixed types. Created with square brackets: [1, 2, 3]
Tuples - Ordered, immutable sequences. Created with parentheses: (1, 2, 3)
Sets - Unordered collections of unique elements. Created with curly braces: {1, 2, 3}
Dictionaries - Key-value pairs for mapping relationships. Created with curly braces and colons: {'name': 'Alice', 'age': 30}
Strings - Immutable sequences of characters, though technically a text type rather than a collection

Collections Module
Python's collections module provides specialized container types:

namedtuple - Tuple subclass with named fields
deque - Double-ended queue for fast appends/pops from both ends
Counter - Dictionary subclass for counting hashable objects
OrderedDict - Dictionary that remembers insertion order 
defaultdict - Dictionary with default values for missing keys
ChainMap - Groups multiple dictionaries into a single view

Other Collection Types

frozenset - Immutable version of set
array (from array module) - Efficient arrays of numeric values
bytes and bytearray - Sequences of bytes
'''
